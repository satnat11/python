# assignment operators

"""
=   assignment
+=  addition
-=  subtraction
*=  multiplication
/= division
%= modulus
**=exponentiation
//=floor division

"""
a=125
print(a)
a+=10 #a=a+10
print(a)
a-=10 # a=a-10
print(a)
a*=10 #a=a*10
print(a)
a/=10 #a=a/10
print(a)
a%=10
print(a)
