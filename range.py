# range in python
"""
 1-5       => 1,2,3,4,5

 range(5)   =>0,1,2,3,4
 range(2,5) =>2,3,4
"""

a=list(range(5))
print(a)   # same output but here create variabe name
print(list(range(5)))   # directly mention here

print(list(range(2,5)))  #n-1
print(list(range(0,21,2)))  # display even no till 21
print(list(range(1,20,2)))  # display odd no till 20


